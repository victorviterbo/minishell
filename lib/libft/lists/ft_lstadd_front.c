#include "libft.h"

void	ft_lstadd_front(t_list **lst, t_list *new_node);

void	ft_lstadd_front(t_list **lst, t_list *new_node)
{
	if (!lst)
		return ;
	new_node->next = *lst;
	*lst = new_node;
	return ;
}
